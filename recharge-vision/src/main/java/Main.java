/*
   JSON format:
   {
       "team": <team number>,
       "ntmode": <"client" or "server", "client" if unspecified>
       "cameras": [
           {
               "name": <camera name>
               "path": <path, e.g. "/dev/video0">
               "pixel format": <"MJPEG", "YUYV", etc>   // optional
               "width": <video mode width>              // optional
               "height": <video mode height>            // optional
               "fps": <video mode fps>                  // optional
               "brightness": <percentage brightness>    // optional
               "white balance": <"auto", "hold", value> // optional
               "exposure": <"auto", "hold", value>      // optional
               "properties": [                          // optional
                   {
                       "name": <property name>
                       "value": <property value>
                   }
               ],
               "stream": {                              // optional
                   "properties": [
                       {
                           "name": <stream property name>
                           "value": <stream property value>
                       }
                   ]
               }
           }
       ]
       "switched cameras": [
           {
               "name": <virtual camera name>
               "key": <network table key used for selection>
               // if NT value is a string, it's treated as a name
               // if NT value is a double, it's treated as an integer index
           }
       ]
   }
 */

public final class Main {
  public static final Main INSTANCE = new Main();
  public CameraServer cameraServer = CameraServer.getInstance();
  public UsbCamera psEye = new UsbCamera("PSEye", "/dev/video0");

  public static void main(String[] args) {
    System.out.println("Starting instance...");
    INSTANCE.start();
  }

  public void start() {
    System.out.println("Hi from Andrew");
    startPSEye();
    System.out.println("Startup complete.");
    // Make program never die
    while (true) {
      try {
        Thread.sleep(10000);
      } catch (InterruptedException ignored) {
        return;
      }
    }
  }

  public void startPSEye() {
    MjpegServer server = cameraServer.startAutomaticCapture(psEye);
    server.setResolution(320, 240);
  }
}
